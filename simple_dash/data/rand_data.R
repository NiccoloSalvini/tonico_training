db = tibble(
  names = name_and_surname
) %>% 
  tidyr::separate(names, sep = " ", into = c("name", "surname")) %>% 
  mutate(
    gender = ifelse(str_match(name, pattern = "\\w\\b") %>%  as.vector() %in% c("a","e","y"),"F" , "M"),
    gender = as_factor(gender),
    weight = rnorm(100, mean = 60, sd = 20), 
    subs = ifelse(weight > 60, T, F),
    height = rnorm(100, mean = 1.50, sd  = 0.30 ),
    bmi_index = weight /height^2,
    weight_class = case_when(
      bmi_index < 16.5 ~ "severe underweight",
      16.5 <= bmi_index &  bmi_index <= 18.4 ~ "underweight",
      18.5 <= bmi_index &  bmi_index <= 24.9 ~ "normal",
      25 <= bmi_index &  bmi_index <= 30 ~ "overweight",
      30.1 <= bmi_index &  bmi_index <= 34.9 ~ "1st obesity",
      35 <= bmi_index &  bmi_index <= 40 ~ "2nd obesity",
      T ~ "3rd obesity",
    ),
    weight_class = as_factor(weight_class)
    ) 
